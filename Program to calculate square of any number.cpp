#include <iostream>

using namespace std;  

int main() 
{  
    double n, ans;  
    cout << "Enter the Number of which you want the Square: ";  
    cin >> n;  
    ans = n * n;  
    cout << "\n" << n << "^2" << " = " << ans;  
    cout << "\n" << "Square of " << n << " is: " << ans;  
    return 0;  
} 
